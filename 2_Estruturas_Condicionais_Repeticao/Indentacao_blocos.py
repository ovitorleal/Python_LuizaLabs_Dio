##Estitica
#Indentar código é uma forma de manter o código fonte mais legível e manutenível.
#Em pyhton ela exerce um segundo papel, através da indentação o interpretador consegue determinar onde um bloco de comando inicia e onde ele termina.
#Exemplo:

def sacar(self, valor: float) -> None:
    if self.saldo >= valor:    #inicio do bloco do if
        self.saldo -= valor    
    #fim do bloco do if
#fim do bloco do método sacar.
    
