#São operadores utilizados para definir o valor inicial ou sobrescrever o valor de uma variável.

#atribuição simples ( = )
saldo = 500 

print(saldo)  # Saída: 500

#Atribuição com adição ( += )
saldo = 500
saldo += 200  # Equivalente a: saldo = saldo + 200
print(saldo)  # Saída: 700

#Atribuição com subtração ( -= )
saldo = 500 
saldo -= 150  # Equivalente a: saldo = saldo - 150
print(saldo)  # Saída: 350

#Atribuição com multiplicação ( *= )
saldo = 500
saldo *= 2  # Equivalente a: saldo = saldo * 2
print(saldo)  # Saída: 1000

#Atribuição com divisão ( /= ) ou divisão inteira ( //= )
saldo = 500
saldo /= 4  # Equivalente a: saldo = saldo / 4
print(saldo)  # Saída: 125.0

saldo = 500
saldo //= 4  # Equivalente a: saldo = saldo // 4
print(saldo)  # Saída: 125


#Atribuição com módulo ( %= )
saldo = 500
saldo %= 300  # Equivalente a: saldo = saldo % 300
print(saldo)  # Saída: 200

